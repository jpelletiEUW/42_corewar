int		ft_sqrt(int nb)
{
	int sq;
	int res;

	sq = 1;
	res = 0;
	while (res < nb)
	{
		if (sq * sq == nb)
			return (sq);
		else
			res = sq * sq;
		sq++;
	}
	return (ft_sqrt(nb - 1));
}
